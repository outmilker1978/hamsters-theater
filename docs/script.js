// === Выбор языка ===
var currentLang = 'ru';

function setLang(l) {
  currentLang = l;
  // Обновить текст всех элементов
  var els = document.querySelectorAll('[data-i18n]');
  for (var i = 0; i < els.length; i++) {
    var key = els[i].getAttribute('data-i18n');
    if (lang[l][key] !== undefined) {
      els[i].innerHTML = lang[l][key];
    }
  }
  // Обновить placeholder
  var phs = document.querySelectorAll('[data-i18n-placeholder]');
  for (var i = 0; i < phs.length; i++) {
    var key = phs[i].getAttribute('data-i18n-placeholder');
    if (lang[l][key] !== undefined) {
      phs[i].setAttribute('placeholder', lang[l][key]);
    }
  }
  // Подсветить кнопку языка
  var btns = document.querySelectorAll('.lang-btn');
  for (var i = 0; i < btns.length; i++) {
    btns[i].classList.toggle('active', btns[i].getAttribute('data-lang') === l);
  }
  // Сохранить
  try { localStorage.setItem('tvh_lang', l); } catch(e) {}
}

// === Кнопки переключения ===
var langBtns = document.querySelectorAll('.lang-btn');
for (var i = 0; i < langBtns.length; i++) {
  langBtns[i].addEventListener('click', function() {
    setLang(this.getAttribute('data-lang'));
  });
}

// === Восстановить язык ===
try {
  var saved = localStorage.getItem('tvh_lang');
  if (saved && (saved === 'ru' || saved === 'en')) {
    setLang(saved);
  }
} catch(e) {}

